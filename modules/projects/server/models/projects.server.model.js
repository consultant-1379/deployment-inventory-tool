var Project = new MongooseSchema({
  pod_id: {
    type: MongooseSchema.ObjectId,
    ref: 'Pod'
  },
  name: {
    type: String,
    required: true,
    validate: commonValidators.objectNameValidator
  },
  id: {
    type: String,
    required: true
  },
  username: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  network: {
    name: {
      type: String,
      required: true
    },
    ipv4_ranges: {
      required: true,
      type: [IPv4RangeSchema]
    },
    ipv6_ranges: {
      required: false,
      type: [IPv6RangeSchema]
    }
  },
  exclusion_ipv4_addresses: {
    required: false,
    type: [ExclusionIPv4AddressSchema]
  },
  exclusion_ipv6_addresses: {
    required: false,
    type: [ExclusionIPv6AddressSchema]
  }
}, { strict: 'throw' });

Project.index({ name: 1, pod_id: 1 }, { unique: true });

Project.index({ name: 1, id: 1 }, { unique: true });

Project.plugin(uniqueValidator, {
  message: 'Error, one of the following pairs are not unique. (Project Name and Pod) ' +
'or (Project Name and Openstack Project Id)'
});

Project.plugin(MongooseHistory);

module.exports.Schema = mongoose.model('Project', Project);
